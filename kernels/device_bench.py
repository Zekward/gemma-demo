#!/usr/bin/env python3
"""Run the Q4_0xQ8_0 vec_dot benchmark on the REAL Apple A9 (iPhone 6s) and return its JSON.

Pipeline: cross-compile bench.c + <candidate.c> into a minimal iOS .app (project generated by
xcodegen), sign+build with xcodebuild automatic signing (mints a provisioning profile against the
connected device on first run), deploy + launch via ios-deploy, and scrape the single JSON line the
bench prints to the device console.

This is the "real A9" backend for kernel_loop.py (host M3 timings are only an ISA-valid proxy).

  python3 device_bench.py --candidate candidate_seed.c --d-in 1152 --rows 2048
Respects env DEVELOPMENT_TEAM (else auto-detects the first Apple Development identity).
"""
from __future__ import annotations
import argparse, json, os, re, subprocess, sys, pathlib, shutil

HERE = pathlib.Path(__file__).resolve().parent
DEV = "/Applications/Xcode.app/Contents/Developer"
BUILD = HERE / "ios_build"
BUNDLE_ID = "com.bondsec.kbench"
MARKER = "KBENCH_JSON "

PROJECT_YML = """
name: kbench
options:
  bundleIdPrefix: com.bondsec
  deploymentTarget:
    iOS: "13.0"
settings:
  base:
    OTHER_CFLAGS: "-mcpu=apple-a9"
    GCC_OPTIMIZATION_LEVEL: "3"
    ENABLE_BITCODE: NO
    ARCHS: arm64
    ONLY_ACTIVE_ARCH: YES
targets:
  kbench:
    type: application
    platform: iOS
    sources:
      - bench.c
      - candidate.c
    info:
      path: Info.plist
      properties:
        UILaunchScreen: {}
        UIApplicationSupportsMultipleScenes: false
    settings:
      base:
        PRODUCT_BUNDLE_IDENTIFIER: __BUNDLE_ID__
        DEVELOPMENT_TEAM: __TEAM__
        CODE_SIGN_STYLE: Automatic
        TARGETED_DEVICE_FAMILY: "1,2"
"""


def sh(cmd, **kw):
    return subprocess.run(cmd, capture_output=True, text=True, **kw)


def detect_team() -> str:
    if os.environ.get("DEVELOPMENT_TEAM"):
        return os.environ["DEVELOPMENT_TEAM"]
    out = sh(["security", "find-identity", "-v", "-p", "codesigning"]).stdout
    m = re.search(r"Apple Development:[^(]*\(([A-Z0-9]{10})\)", out)
    if not m:
        sys.exit("no Apple Development signing identity found (Xcode > Settings > Accounts)")
    return m.group(1)


def device_udid() -> str | None:
    out = sh(["idevice_id", "-l"]).stdout.strip()
    return out.splitlines()[0] if out else None


def setup_project(team: str, candidate: str):
    BUILD.mkdir(exist_ok=True)
    shutil.copy(HERE / "bench.c", BUILD / "bench.c")
    shutil.copy(candidate, BUILD / "candidate.c")
    (BUILD / "project.yml").write_text(
        PROJECT_YML.replace("__BUNDLE_ID__", BUNDLE_ID).replace("__TEAM__", team))
    g = sh(["xcodegen", "generate"], cwd=str(BUILD))
    if not (BUILD / "kbench.xcodeproj").exists():
        sys.exit(f"xcodegen failed:\n{g.stdout}\n{g.stderr}")


def build(team: str) -> pathlib.Path:
    env = {**os.environ, "DEVELOPER_DIR": DEV}
    cmd = ["xcodebuild", "-project", str(BUILD / "kbench.xcodeproj"), "-scheme", "kbench",
           "-configuration", "Debug", "-sdk", "iphoneos", "-allowProvisioningUpdates",
           "-derivedDataPath", str(BUILD / "dd"), f"DEVELOPMENT_TEAM={team}", "build"]
    p = subprocess.run(cmd, capture_output=True, text=True, env=env)
    app = BUILD / "dd" / "Build" / "Products" / "Debug-iphoneos" / "kbench.app"
    if not app.exists():
        sys.stderr.write(p.stdout[-3500:] + "\n--- stderr ---\n" + p.stderr[-1500:] + "\n")
        sys.exit("xcodebuild failed to produce kbench.app (see log above)")
    return app


def deploy_and_run(app: pathlib.Path, env_args: dict, timeout=240) -> dict:
    env = {**os.environ, **env_args}
    cmd = ["ios-deploy", "--noninteractive", "--debug", "--bundle", str(app)]
    try:
        p = subprocess.run(cmd, capture_output=True, text=True, timeout=timeout, env=env)
        out = p.stdout + p.stderr
    except subprocess.TimeoutExpired as e:
        out = (e.stdout or "") + (e.stderr or "")
    r = _scrape(out)
    if r is None:
        sys.stderr.write("--- ios-deploy tail ---\n" + out[-3000:] + "\n")
        return {"ok": False, "error": "no JSON marker in device output"}
    return r


def _scrape(out: str) -> dict | None:
    for line in out.splitlines():
        i = line.find(MARKER)
        if i != -1:
            try:
                return json.loads(line[i + len(MARKER):].strip())
            except Exception:
                pass
    return None


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--candidate", default=str(HERE / "candidate_seed.c"))
    ap.add_argument("--d-in", type=int, default=1152)
    ap.add_argument("--rows", type=int, default=2048)
    args = ap.parse_args()

    team = detect_team()
    udid = device_udid()
    if not udid:
        sys.exit("no iOS device connected (idevice_id -l empty). Plug in + unlock + Trust the 6s.")
    sys.stderr.write(f"[device] udid={udid} team={team}\n")
    setup_project(team, args.candidate)
    app = build(team)
    sys.stderr.write(f"[device] built {app.name}; deploying to phone...\n")
    res = deploy_and_run(app, {"D_IN": str(args.d_in), "ROWS": str(args.rows), "MIN_SECS": "0.5"})
    print(json.dumps(res))


if __name__ == "__main__":
    main()
